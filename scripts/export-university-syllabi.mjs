// export-university-syllabi.mjs
// Dumps the entire `university_syllabi` table from prod to
// supabase/seeds/university_syllabi.sql as idempotent INSERT statements.
//
// Why this script exists:
//   The 680+ rows in `university_syllabi` were seeded directly in Supabase
//   Studio, not via a committed migration. If the DB ever rebuilds from
//   scratch the syllabi vanish. This script snapshots them into a replayable
//   SQL file committed to the repo.
//
// The generated SQL:
//   - Wraps everything in BEGIN / COMMIT
//   - DELETEs all rows for every university_name present in the snapshot
//     (so re-applying the seed refreshes cleanly and does not double-insert;
//     rows from universities outside the snapshot are untouched)
//   - Leaves `id` to DEFAULT so the seed is not tied to specific UUIDs
//
// Run:
//   node scripts/export-university-syllabi.mjs
//   → writes supabase/seeds/university_syllabi.sql
//
// Re-run after any change to the catalogue (including seed-aicte-mech-auto.mjs)
// so the committed seed file stays in sync with prod.
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { resolve } from 'path';

function loadEnv() {
  const candidates = ['.env.local', 'website/.env.local', '../.env.local', 'admin/.env.local', '.env'];
  for (const path of candidates) {
    try {
      const parsed = Object.fromEntries(
        readFileSync(path, 'utf8').split('\n')
          .filter(l => l && !l.startsWith('#') && l.includes('='))
          .map(l => {
            const idx = l.indexOf('=');
            return [l.slice(0, idx).trim(), l.slice(idx + 1).trim().replace(/^["']|["']$/g, '')];
          })
      );
      if (parsed.SUPABASE_SERVICE_ROLE_KEY) return { ...process.env, ...parsed };
    } catch { /* try next */ }
  }
  return { ...process.env };
}

const env = loadEnv();
const url = env.SUPABASE_PROJECT_URL || env.SUPABASE_URL || env.NEXT_PUBLIC_SUPABASE_URL;
const key = env.SUPABASE_SERVICE_ROLE_KEY;

if (!url || !key) {
  console.error('Missing SUPABASE_PROJECT_URL (or SUPABASE_URL) + SUPABASE_SERVICE_ROLE_KEY in .env.local');
  process.exit(1);
}

const headers = {
  apikey: key,
  Authorization: `Bearer ${key}`,
  'Content-Type': 'application/json',
};

function sqlLiteral(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? 'TRUE' : 'FALSE';
  return `'${String(v).replace(/'/g, "''")}'`;
}

const PAGE_SIZE = 1000;

async function fetchAll() {
  const rows = [];
  let offset = 0;
  // Order deterministic so the output SQL has a stable diff
  const query = 'select=*&order=university_name.asc,degree.asc,specialisation.asc.nullsfirst,year.asc,semester.asc,paper_code.asc';
  while (true) {
    const res = await fetch(`${url}/rest/v1/university_syllabi?${query}`, {
      headers: { ...headers, Range: `${offset}-${offset + PAGE_SIZE - 1}`, 'Range-Unit': 'items' },
    });
    if (!res.ok) throw new Error(`Fetch failed: ${res.status} ${await res.text()}`);
    const batch = await res.json();
    rows.push(...batch);
    if (batch.length < PAGE_SIZE) break;
    offset += PAGE_SIZE;
  }
  return rows;
}

const cols = [
  'university_name', 'degree', 'specialisation', 'year', 'semester',
  'paper_code', 'paper_name', 'paper_type', 'credits', 'primary_saathi', 'framework',
];

const rows = await fetchAll();
console.log(`Fetched ${rows.length} rows`);

const universityNames = [...new Set(rows.map(r => r.university_name))].sort();
const generatedAt = new Date().toISOString();

let out = `-- Auto-generated by scripts/export-university-syllabi.mjs
-- Snapshot of public.university_syllabi — ${generatedAt}
-- Total rows: ${rows.length}
-- Universities / frameworks covered:
${universityNames.map(n => `--   - ${n}`).join('\n')}
--
-- Apply via Supabase SQL editor or psql to restore the curriculum catalogue.
-- Idempotent: wipes rows for every university_name in this snapshot, then
-- re-inserts. Rows from universities outside this set are untouched. The
-- id column is left to DEFAULT so the seed is not tied to specific UUIDs.

BEGIN;

DELETE FROM public.university_syllabi
 WHERE university_name IN (
   ${universityNames.map(n => sqlLiteral(n)).join(',\n   ')}
 );

INSERT INTO public.university_syllabi
  (${cols.join(', ')})
VALUES
`;

const valueLines = rows.map(r =>
  `  (${cols.map(c => sqlLiteral(r[c])).join(', ')})`
);
out += valueLines.join(',\n');
out += ';\n\nCOMMIT;\n';

const outDir = resolve('supabase/seeds');
mkdirSync(outDir, { recursive: true });
const outPath = resolve(outDir, 'university_syllabi.sql');
writeFileSync(outPath, out, 'utf8');
console.log(`✓ Wrote ${outPath} (${(out.length / 1024).toFixed(1)} KB)`);
